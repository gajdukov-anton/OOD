namespace Composite.Shape
{
    public struct Rect
    {
        public double left;
        public double width;
        public double top;
        public double height;

        public Rect(double left, double width, double top, double height)
        {
            this.left = left;
            this.width = width;
            this.top = top;
            this.height = height;
        }
    }
}
